func parseHexColor(s string) color.RGBA {
	c := color.RGBA{
		A: 0xff,
	}
	c.R = hexToByte(s[1:3])
	c.G = hexToByte(s[3:5])
	c.B = hexToByte(s[5:7])
	return c
}

func hexToByte(s string) uint8 {
	b1 := hexCharToByte(s[0])
	b2 := hexCharToByte(s[1])
	return (b1 << 4) | b2
}

func hexCharToByte(c byte) uint8 {
	switch {
	case c >= '0' && c <= '9':
		return c - '0'
	case c >= 'a' && c <= 'f':
		return c - 'a' + 10
	case c >= 'A' && c <= 'F':
		return c - 'A' + 10
	}
	return 0
}
